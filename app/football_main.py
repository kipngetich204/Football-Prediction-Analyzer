# Firebase helpers live here so the pipeline can store results outside the app.
def init_firebase() -> firestore.Client:
    """
    Initialise Firebase Admin SDK (safe to call multiple times).

    Preferred: set FIREBASE_CREDS to the full service-account JSON (as one
    string) — this is what CI/GitHub Actions should use.

    Fallback (local dev): set individual FIREBASE_* env vars.
    """
    if not firebase_admin._apps:
        firebase_creds_raw = os.environ.get("FIREBASE_CREDS")

        if firebase_creds_raw:
            service_account = json.loads(firebase_creds_raw)
            cred = credentials.Certificate(service_account)
        else:
            private_key = os.environ["FIREBASE_PRIVATE_KEY"].replace("\\n", "\n")
            service_account = {
                "type": "service_account",
                "project_id":           os.environ["FIREBASE_PROJECT_ID"],
                "private_key_id":       os.environ["FIREBASE_PRIVATE_KEY_ID"],
                "private_key":          private_key,
                "client_email":         os.environ["FIREBASE_CLIENT_EMAIL"],
                "client_id":            os.environ["FIREBASE_CLIENT_ID"],
                "auth_uri":             "https://accounts.google.com/o/oauth2/auth",
                "token_uri":            "https://oauth2.googleapis.com/token",
                "client_x509_cert_url": os.environ["FIREBASE_CLIENT_CERT_URL"],
            }
            cred = credentials.Certificate(service_account)

        firebase_admin.initialize_app(cred)
        logger.info("Firebase: initialised")

    return firestore.client()


def append_to_firestore(result: dict) -> None:
    """
    Append prediction results to the `dailyTips` Firestore collection.

    Each match prediction is written as its own document.
    If result contains a top-level `predictions` list, each item becomes
    a document; otherwise the whole result dict is written as one document.
    Document IDs are auto-generated by Firestore (add, not set).
    """
    db = init_firebase()
    collection = db.collection("dailyTips")
    date = result.get("date", "unknown")

    predictions = result.get("predictions")
    if isinstance(predictions, list) and predictions:
        docs = predictions
    else:
        docs = [result]

    written = 0
    for doc in docs:
        try:
            doc_data = {**doc, "date": date, "createdAt": firestore.SERVER_TIMESTAMP}
            collection.add(doc_data)
            written += 1
        except Exception as e:
            logger.error(f"Failed to write document to Firestore: {e}")

    logger.info(f"Firestore: {written}/{len(docs)} document(s) written to 'dailyTips'")


def should_run_pipeline(current_fetched_data: object) -> dict:
    """Decide whether the current payload is usable for one run."""

    if not current_fetched_data:
        return {"shouldRun": False, "reason": "Data is empty or invalid", "action": "skip"}
    if not isinstance(current_fetched_data, dict):
        return {"shouldRun": False, "reason": "Data is not a valid JSON object", "action": "skip"}
    if not isinstance(current_fetched_data.get("matches", []), list):
        return {"shouldRun": False, "reason": "Data contains an invalid matches field", "action": "skip"}

    return {"shouldRun": True, "reason": "Starting a fresh run", "action": "run"}


# Pull the latest daily match payload from the backend service.
def get_daily_matches() -> dict | None:
    """Synchronous fetch from the backend API."""
    try:
        logger.info("Fetching daily matches from API...")
        response = requests.get("https://backend-livetips.onrender.com/daily")

        if response.status_code == 200:
            return response.json()

        logger.warning(
            f"API returned non-200 status: {response.status_code}"
        )
    except requests.RequestException as e:
        logger.warning(f"Could not fetch from API ({e})")
    return None


# Load payload data from the live API.
def load_payload() -> dict:
    """Always fetch the match payload from the live backend API."""
    data = get_daily_matches()
    return data if data else {}


# Run the full prediction pipeline from the command line.
def main():
    logger.info("FOOTBALL PREDICTION ANALYZER — Agent-Based Web Research")

    payload = load_payload()

    total = payload.get("total", len(payload.get("matches", [])))
    logger.info(f"\n  Date    : {payload.get('date', 'Unknown')}")
    logger.info(f"  Matches : {total}")

    # Decide whether the pipeline should run for the current payload.
    decision = should_run_pipeline(payload)

    logger.info(f"\nController decision → {json.dumps(decision)}")

    if not decision["shouldRun"]:
        logger.info(f"Pipeline SKIPPED: {decision['reason']}")
        return

    logger.info(f"Pipeline RUNNING: {decision['reason']}")

    # Run the prediction agent for every eligible match.
    logger.info("\nStarting research agent...\n")
    try:
        agent = FootballPredictionAgent(
            groq_api_key=os.getenv("GROQ_API_KEY")
        )
    except ValueError as exc:
        logger.error(str(exc))
        return

    result = agent.predict_all_matches(payload)

    # Send the results to Firestore for storage and later access.
    append_to_firestore(result)

    logger.info("=" * 70)


if __name__ == "__main__":
    main()